						<h4>15. POSTANOWIENIA KOŃCOWE</h4>
						1) Rajd odbędzie się bez względu na warunki atmosferyczne.<br/>
						2) Organizator zastrzega sobie prawo do skrócenia tras Rajdu ze względu na złe warunki atmosferyczne.<br/>
						3) Organizator zastrzega sobie prawo do odwołania Rajdu lub znaczącej zmiany jego tras w przypadku wystąpienia znaczącego zagrożenia pożarowego lub wystąpienia silnych wiatrów.<br/>
						3) Organizator zastrzega sobie prawo do odwołania Rajdu w związku z wystąpieniem klęsk żywiołowych lub innych czynników uznawanych za działanie “siły wyższej”.<br/>
						4) Udział w Rajdzie jest dobrowolny. Uczestnicy startują na własną odpowiedzialność i w razie wypadku nie będą dochodzić odszkodowania od Organizatora.<br/>
						5) Obowiązuje bezwzględny zakaz pomocy osób trzecich, w tym korzystania z jakichkolwiek środków transportu (z wyjątkiem roweru na trasie TR).<br/>
						6) Zawodnicy mogą uzupełniać napoje i żywność w napotkanych po drodze sklepach oraz na wskazanych przez Organizatora PK.<br/>
						7) Ze względów bezpieczeństwa, wszystkich uczestników bezwzględnie obowiązuje zgłoszenie Organizatorowi zakończenia udziału w Rajdzie nie później niż godzinę po zakończeniu limitu czasu dla trasy na której startuje, w jeden z poniższych sposobów:<br/>
						•	poprzez zameldowanie się w bazie Rajdu,<br/>
						•	telefonicznie sędziemu głównemu (numer telefonu będzie podany na mapie).<br/>
						9) Organizator nie odpowiada za szkody wyrządzone uczestnikom przez innych uczestników, a także za szkody wyrządzone uczestnikom przez osoby trzecie.<br/>
						10) Zabronione jest pozostawianie śmieci i odpadków na trasie poza miejscami wyznaczonymi.<br/>
						11) Na Rajdzie obowiązują zasady fair play. Utrudnianie marszu lub jazdy rowerem innym uczestnikom Rajdu, wprowadzanie ich w błąd, a w szczególności niszczenie lub zasłanianie oznaczeń PK oraz zabieranie przyrządów do potwierdzania obecności na PK jest niedozwolone i karane będzie dyskwalifikacją.<br/>
						12) Używanie urządzeń do nawigacji satelitarnej jako pomocy przy nawigacji jest zabronione i będzie karane dyskwalifikacją. Używanie urządzeń do nawigacji satelitarnej dozwolone jest jedynie w celu zapisu śladu (tracka) do wykorzystania po Rajdzie oraz do pomiaru parametrów ruchu (prędkości, kierunku i odległości). Ewentualne protesty dotyczące sytuacji mających miejsce w trakcie Rajdu można składać Sędziemu Głównemu do godziny 22.00 w dniu 10 czerwca 2016 r. Protesty zostaną rozpatrzone do godziny 23.00, po czym zostaną ogłoszone wstępne wyniki Rajdu.<br/>
						13) Uczestnicy mają prawo zgłaszać protesty dotyczące wyników Rajdu przez 3 dni od momentu ukazania się wyników na stronie internetowej Rajdu.  Po rozpatrzeniu protestów zgłoszonych do wyników Rajdu, wyniki uznawać będzie się za ostateczne. Organizator nie bierze odpowiedzialności za rzeczy pozostawione przez uczestników w bazie Rajdu.<br/>
						14) Organizator zastrzega sobie prawo do zmiany niniejszego regulaminu, o czym uczestnicy zostaną poinformowani najpóźniej przed startem danej trasy.<br/>
						15) Ostateczna interpretacja niniejszego regulaminu należy do Organizatora. W sprawach spornych decyzja kierownika Rajdu jest decyzją ostateczną.<br/>
						16) Uczestnicy Rajdu wyrażają zgodę na publiczne ogłoszenie wyników wraz z jego danymi osobowymi w postaci imienia, nazwiska, roku urodzenia i miejscowości podanych w zgłoszeniu, a także na publikację ich wizerunku oraz ich imienia i nazwiska w relacjach z Rajdu zamieszczanych w mediach oraz materiałach promocyjnych Organizatora, Patronów i Sponsorów oraz wyrażają zgodę na przetwarzanie ich danych osobowych na potrzeby organizowanych przez Organizatora imprez i rajdów, jak również na ich gromadzenie w bazach danych zgodnie z przepisami ustawy z dnia 29 sierpnia 1997 o ochronie danych osobowych (Dz.U. 2016 nr 0 poz. 922 Uczestnicy Rajdu zgadzają się również na otrzymywanie korespondencji drogą elektroniczną i  udostępniają w tym celu swój adres e-mail zgodnie z ustawą z dnia 18 lipca 2002 r. o świadczeniu usług drogą elektroniczną (Dz.U. 2016 poz. 1030).<br/>
						17) Nieznajomość niniejszego regulaminu nie zwalnia uczestnika z jego przestrzegania.<br/>
