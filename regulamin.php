						<h4>4. TERMIN I MIEJSCE RAJDU</h4>
						Rajd odbędzie się w dniu 24 czerwca 2017 r. w Osiecznej niedaleko Czerska.<br/>
						Baza Rajdu:  Zespół Szkół Publicznych w Osiecznej ul. Mickiewicza 1<br/><br/>
						 
						<h4>5. KATEGORIE STARTOWE I LIMITY CZASU</h4>
						W ramach Rajdu zostaną przeprowadzone następujące konkurencje:<br/>
						•	TR 100  trasa rowerowa o długości około 100 km, limit czasu 12 h,<br/>
						•	TR 50 trasa rowerowa o długości około 50 km, limit czasu 7 h,<br/>
						•	TP 50 trasa piesza o długości około 50 km, limit czasu 13 h,<br/>
						•	TP 25 trasa piesza o długości około 25 km, limit czasu 9 h.<br/>
						•	TPR trasa piesza rekreacyjna o długości około 8 – 10 km, limit czasu 5 h<br/>
						Długości tras są liczone wzdłuż przejezdnych/przebieżnych elementów liniowych istniejących na mapie.<br/><br/>
						 
						<h4>6. KLASYFIKACJA W PUCHARZE POLSKI</h4>
						1)      Trasa piesza (TP 50) Bałtyckiej Tułaczki zaliczana jest do klasyfikacji Pucharu Polski w Pieszych Maratonach na Orientację na dystansie 50 km. W ramach niniejszego rajdu rozgrywane są Mistrzostwa Polski w Pieszych Maratonach na Orientację 2017 na dystansie 50 km (MP50km)<br/>
						2)      Trasa rowerowa (TR 100) Mazurskich Tropów zaliczana jest do klasyfikacji Pucharu Polski w Maratonach Rowerowych na Orientację.<br/><br/>					 
						<h4>7. ZASADY WSPÓŁZAWODNICTWA</h4>
						1) Zadaniem uczestniczących w Rajdzie będzie pokonanie trasy wyznaczonej w terenie w postaci punktów kontrolnych (PK) zaznaczonych na mapie w jak najkrótszym czasie. Droga pomiędzy PK jest dowolna, a jej wybór należy do uczestników.<br/>
						2) PK oznaczone będą biało – czerwonymi lampionami.<br/>
						3) Lokalizacja PK oznaczona będzie na mapach rozdanych uczestnikom przed startem.<br/>
						4) Obecność na PK uczestnicy potwierdzają za pomocą perforatorów, w odpowiednich miejscach na karcie startowej.<br/>
						5) W przypadku braku na danym PK lampionu z perforatorem należy wpisać na karcie startowej “BPK”, zebrać konfetti leżące na ziemi pod PK. Zaleca się  także wykonać zdjęcie miejsca, w którym w ocenie zawodnika powinien znajdować się lampion oraz kontakt telefoniczny z Organizatorem.<br/>
						6) Każdy z zawodników powinien odwiedzić PK osobiście.<br/>
						7) Komunikat techniczny na temat szczegółów rywalizacji w ramach Rajdu będzie opublikowany na stronie www.baltyckatulaczka.pl do dnia 20 czerwca 2017 r.<br/><br/>
						<h4>8. ZASADY KLASYFIKACJI</h4>
						1) Na wszystkich trasach, oprócz trasy rekreacyjnej, prowadzona będzie klasyfikacja z podziałem na kategorie kobiet i mężczyzn.<br/>
						2)  W celu sklasyfikowania w Rajdzie należy na wybranej przez siebie trasie prawidłowo potwierdzić co najmniej jeden PK.<br/>
						3) O kolejności w klasyfikacji Rajdu decyduje liczba prawidłowo zaliczonych PK, natomiast w przypadku równej liczby PK – czas pokonania trasy.<br/>
						4) Za przybycie na metę po wyznaczonym limicie czasu naliczane będą punkty karne; za każdą rozpoczęte 10 minut spóźnienia odejmowany będzie jeden PK, przy czym w przypadku przybycia na metę później niż po upływie 30 minut od upływu limitu obowiązującego na danej trasie zawodnik nie będzie klasyfikowany.<br/>
						5) Nieklasyfikowani będą uczestnicy, którzy nie przestrzegają zasad sportowego zachowania, nie zaliczyli żadnego PK, będą się poruszali po drogach wyłączonych z Rajdu, nie zwrócą karty startowej po przybyciu na metę.<br/><br/>
						 
						<h4>9. WYPOSAŻENIE ZALECANE</h4>
						1) Uczestnik TP powinien posiadać telefon komórkowy z numerem telefonu podanym w zgłoszeniu, kompas, długopis, zegarek oraz odzież chroniąca przed zimnem i deszczem.<br/>
						2) Uczestnik TR powinien posiadać telefon komórkowy z numerem telefonu podanym w zgłoszeniu, sprawny rower (wyposażony w oświetlenie na przód i tył), sztywny kask, podstawowy zestaw naprawczy (zapasowa dętka lub łatki, pompkę), mapnik rowerowy, kompas, długopis, zegarek oraz odzież chroniąca przed zimnem i deszczem.<br/><br/>
						 
						<h4>10. MAPY</h4>
						Rajd odbędzie się na mapach dostarczonych przez Organizatora w skali 1:50.000.  Zabrania się używania innych map niż te dostarczone przez Organizatora (w tym w szczególności map w postaci cyfrowej w urządzeniach nawigacji satelitarnej). Uczestnik, który będzie używał niedozwolonych map zostanie zdyskwalifikowany.<br/><br/>
						 
