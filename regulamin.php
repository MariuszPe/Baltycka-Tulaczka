						4) Osoby poniżej 16 roku życia zobowiązane będą wziąć udział w Rajdzie pod opieką osoby, która ukończyła 21 roku życia.<br/>
						5) Zgłoszenia na Rajd do dnia 20.06.2016 r. można dokonać poprzez formularz na stronie internetowej Rajdu. Po tym terminie można się zgłosić na starcie Rajdu tylko po uzgodnieniu z organizatorem. W takich przypadkach nie gwarantujemy pełnych świadczeń.<br/><br/>
						 
