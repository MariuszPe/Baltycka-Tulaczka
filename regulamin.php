						<h4>1. CELE RAJDU</h4>
						Rajd „Bałtycka Tułaczka” (zwany dalej Rajdem) jest imprezą o charakterze sportowo-turystycznym. Celami Rajdu  są:<br/><br/>
						•	sprawdzenie własnych możliwości psychicznych i fizycznych,<br/>
						•	popularyzacja sportu oraz imprez na orientację jako sposobu spędzania wolnego czasu,<br/>
						•	wymiana doświadczeń przez sympatyków imprez o takim charakterze,<br/>
						•	potwierdzenie w praktyce własnych umiejętności w posługiwaniu się mapą i kompasem.<br/><br/>
						 
						<h4>2. ORGANIZATOR</h4>
						Organizatorem Rajdu jest Studencki Klub Turystyczny przy Politechnice Gdańskiej oraz Stowarzyszenie „Podróżnicy”, zwani dalej łącznie Organizatorem.
						Strona internetowa Rajdu: www.baltyckatulaczka.pl <br/><br/>
						 
