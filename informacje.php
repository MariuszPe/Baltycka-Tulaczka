					<h2>Czym jest rajd na orientację</h2>

					<p>Bałtycka Tułaczka to terenowe zawody na orientację z trasami rowerowymi i pieszymi (lub biegowymi – jak kto woli). Zadaniem uczestników jest odnalezienie w terenie punktów kontrolnych w określonym limicie czasu, przy pomocy mapy i kompasu.</p>

					<p>Mapy z zaznaczoną lokalizacją punktów kontrolnych rozdawane są uczestnikom przed startem. Odnalezienie punktu kontrolnego potwierdza się na karcie startowej przy pomocy specjalnego perforatora, wiszącego na punkcie kontrolnym.</p>

					 

					<h2>Jakie są trasy?</h2>

					Na Bałtyckiej Tułaczce do wyboru są następujące trasy:

						TP25 – trasa piesza (do 25 km)
						TP50 – trasa piesza (do 50 km)
						TR50 – trasa rowerowa (do 50 km)
						TR100 – trasa rowerowa (do 100 km)
						Trasa rekreacyjna – trasa piesza (8 – 10 km)

					<p>Dystanse podane przy każdej z tras, oznaczają liczbę km jakie pokona osoba, która odnajdzie wszystkie punkty kontrolne. Oczywiście nie jest konieczne odnalezienie wszystkich punktów – zaliczyć można ich tyle, na ile starczy chęci, czasu i sił. By zostać sklasyfikowanym wystarczy zaliczyć tylko 1 punkt kontrolny.</p>

					<p>Na trasie rekreacyjnej nie prowadzi się klasyfikacji. Trasa jest prosta nawigacyjnie, a zaznaczone na mapie obiekty kontrolne zlokalizowane są w łatwych do odnalezienia miejscach.  Jest to doskonała okazja do poznania interesujących miejsc w gminie Osieczna. Polecana jest całym rodzinom, które chcą rozpocząć swoją przygodę z turystyką krajoznawczą i rajdami na orientację.</p>
					

					<h2>Kto może wystartować?</h2>

					<p>Rajd przeznaczony jest dla wszystkich – bez względu na wiek i kondycję. Zarówno dla tych, którzy pragną sprawdzić wydolność swojego organizmu, pobić swój kolejny rekord na danym dystansie, jak również tych, którzy po prostu chcą aktywnie i ciekawie spędzić czas na łonie przyrody. Można startować solo, w parach lub większych grupach.</p>
				

					<h2>Jak się poruszać w terenie, jak nawigować?</h2>

					<p>Rajdy na orientację łączą w sobie wiele elementów – nie chodzi tu tylko o tzw. mocną łydkę, ale także liczy się spryt i spostrzegawczość. Poruszanie się pomiędzy punktami jest dowolne (tzw. „scorelauf”), co oznacza, że uczestnicy sami wybierają kolejność zdobywania punktów i trasę przejazdu lub przejścia między nimi.</p>

					<p>Do nawigacji w terenie każdemu uczestnikowi przyda się kompas. Za jego pomocą sprawdzisz czy poruszasz się w dobrym kierunku.</p>

					 

					 
				
				
